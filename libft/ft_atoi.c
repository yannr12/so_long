#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	size_t	neg;
	size_t	result;

	i = 0;
	neg = 1;
	result = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
				neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - 48);
		i++;
	}
	if (neg)
		return (result *= neg);
	return ((int)result);
}
